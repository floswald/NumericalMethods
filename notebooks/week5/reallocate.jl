## Integer Programming Problem

`DM(g,r)` is some sort of score (it's short for *dead miles*). We want to minimize the sum of all scores by choosing `x` in a feasible way.

$$\begin{align}
    \min_{\mathbf{x}(g,r)\in \{0,\mathbb{N}\}^{G\times R}}
    \sum_{g=1}^{G}\sum_{r=1}^{R}&\mathbf{x}(g,r) \times DM(g,r)\\
s.t.& \nonumber\\
\sum_{g=1}^{G} \mathbf{x}(g,r)&=PVR(r), \quad \forall r\\
\sum_{r=1}^{R} \mathbf{x}(g,r)&\leq cap(g), \quad \forall g\\
\end{align}$$

