function build_model(d::Dict)
    @info """
    problem is to allocate $(d[:totalbuses]) buses to $(length(unique(d[:ds][:,:Route]))) routes.
    problem size:
    * we have $(length(unique(d[:ds][:,:Route]))) routes
    * we have $(length(unique(d[:ds][:,:garage_code]))) garages
    choice vector is of size $(nrow(d[:ds])) (route-garage pairs). 
    each element is an Integer ‚àà {0,1,2,..., $(maximum(d[:cap].PVR))},
    where $(maximum(d[:cap].PVR)) is the maximum capacity in the data.
    
    objective: minimize total duration of drivetime
    constraints:
    1. Operational constraint: each route must have the required number of buses (pvr_tenderroute)
    2. Capacity constraint: each garage must not exceed its capacity
    max capacity of biggest garage is $(maximum(d[:cap].PVR))
    """
    maxcap = maximum(d[:cap].PVR)
    garages = unique(d[:ds].garage_code)
    N = length(garages)
    
    routes = unique(d[:ds][:,:Route])  # id of routes
    R = length(routes)  # number of routes

    # duration matrix
    # each row is a route, each column is a garage
    minutes = reshape(d[:ds][:,:duration],N,R)  # minutes[i,j] is the drivetime from garage i to route j
    kms = reshape(d[:ds][:,:distance] ./ 1000,N,R)  # kms[i,j] is the drivetime from garage i to route j

    @info "R*N = $(R*N) = $(R) * $(N)"
    m = Model(GLPK.Optimizer)
    set_optimizer_attribute(m, MOI.Silent(), true)

    @variable(m, 0 <= x[j in 1:N, r in 1:R] <= maxcap, Int, start = 1)
    
    # Objective: minimize total drivetime
    @objective(m, Min,sum(sum( minutes[j,r] * x[j,r] for j in 1:N) for r in 1:R))
    
    # Operational Constraint
    @constraint(m, [r in 1:R], sum(x[j,r] for j in 1:N) == d[:pvrs][r,:required_pvr])

    # has to involve ALL possible garages!
    @constraint(m, [j in 1:N], sum(x[j,r] for r in 1:R) <= d[:cap].PVR[j])

    # cannot allocate more buses than we have
    @constraint(m, sum(x) <= d[:totalbuses])

    # # Solve problem using MIP solver
    JuMP.optimize!(m)

    s = JuMP.solution_summary(m)

    z = value.(x)

    # those garages are shut down
    down = garages[findall(sum(z, dims = 2) .== 0.0)]

    # summary dataframe
    o = @chain DataFrame(garage_code = garages, 
                         buses_allocated = Int.(sum(z,dims = 2))[:],
                         total_duration = sum(minutes .* z, dims = 2)[:],
                         total_km = sum(kms .* z, dims = 2)[:]) begin
        transform(:garage_code => ByRow( ‚àà(down) ) => :isdown)
    end

    # garage allocation dataframe
    g = DataFrame(garage_code = repeat(garages, outer = R), 
                  route_id = repeat(routes, inner = N), 
                  buses_allocated = z[:])

    (o,g)

end



