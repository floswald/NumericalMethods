md"""
# Projection Methods

This is solution class related to policy function iteration. let us quickly reintroduce PFI in the Growth Model:

1. We want to find the function $c^*$ for

$$c^*(k) = \arg \max_z u(z) + \beta V(f(k)-z) \text{  }\forall k\in[0,\infty]$$

2. We **don't** directly solve the maximiation problem outlined above (by, say, trying out different values of choice variable $z$), but it's first order condition:

$$\begin{align}
u'(c^*(k_t)) & = \beta u'(c^*(k_{t+1})) f'(k_{t+1}) \\
             & = \beta u'[c^*(f(k_{t})-c^*(k_t))] f'(f(k_{t})-c^*(k_t)) 
\end{align}$$

3. That is, find a function $c^*$ for which this equation holds.
4. In other words, let's find the *zeros* or *roots* of this function $g$ here (it just puts everything on the left side of equal sign):

$$g(k_t) = u'(c^*(k_t)) - \beta u'[c^*(f(k_{t})-c^*(k_t))] f'(f(k_{t})-c^*(k_t))$$
"""

# ╔═╡ 11ef6295-2b76-4635-b67e-10b73e39278f
md"""
## PFI Recap

* Remember how we did PFI?

1. Set a grid for $k$
2. Propose a candidate policy `c0` defined on each grid point
3. find $x_i$ s.t. $g(x_i) = 0$
4. The set of $x_i$'s is our next policy. 
5. Iterature until convergence.

"""

# ╔═╡ 62acaafe-00f1-4953-bd56-e93c0cb63d4a
begin
	uprime(x) = 1.0 ./ x
	fprime(x) = alpha * x.^(alpha-1)

	alpha     = 0.65
	beta      = 0.95
	grid_max  = 2  # upper bound of capital grid
	n         = 150  # number of grid points
	N_iter    = 3000  # number of iterations
	kgrid     = 1e-2:(grid_max-1e-2)/(n-1):grid_max  # equispaced grid
	f(x) = x^alpha  # defines the production function f(k)
	tol = 1e-9
end

# ╔═╡ 29f092f0-ba03-4374-9f70-02082e3c55d6
begin
	ab        = alpha * beta
	c1        = (log(1 - ab) + log(ab) * ab / (1 - ab)) / (1 - beta)
	c2        = alpha / (1 - ab)
	# optimal analytical values
	v_star(k) = c1 .+ c2 .* log.(k)  
	k_star(k) = ab * k.^alpha   
	c_star(k) = (1-ab) * k.^alpha  
	ufun(x) = log.(x)
end

# ╔═╡ 6bf75b4c-a2d5-43af-9c2a-ff4e75dad548
function policy_iter(grid,c0,u_prime,f_prime)
	c1  = zeros(length(grid))     # next guess
	pol_fun = extrapolate(interpolate((collect(grid),), c0, Gridded(Linear()) ) , Interpolations.Flat())

	# loop over current states
	# of current capital
	for (i,k) in enumerate(grid)
		objective(c) = u_prime(c) - beta * u_prime(pol_fun(f(k)-c)) * f_prime(f(k)-c)
		c1[i] = fzero(objective, 1e-10, f(k)-1e-10) 
	end
	return c1
end

# ╔═╡ 9299d1e8-e860-4950-bc3f-80da4d941714
function PFI(;N_iter = 100)
    c_init = kgrid
    for iter in 1:N_iter
        c_next = policy_iter(kgrid,c_init,uprime,fprime)  
        # check convergence
        if maximum(abs,c_init.-c_next) < tol
            perrors =  maximum(abs,c_next.-c_star(kgrid))
            return (c_next,perrors, iter)
        elseif iter==N_iter
            warn("No solution found after $iter iterations")
            return c_next
        end
        c_init = c_next  # update guess 
    end
end

# ╔═╡ bda9d24f-8a72-4caf-84a1-35a17cafa5ff
function plotPFI()
    v,er,i = PFI()
    plot(kgrid,[v v.-c_star(kgrid)],
            lab=["policy" "error"],
            legend=:bottomright,
            layout = 2, title = ["$i iterations" "Max error"])
end

# ╔═╡ 3d388326-6b77-4bf9-8116-553e2e70c1eb
plotPFI()

