function make_trans(Œ∏, n)
	transition = zeros(n, n);
	p = [Œ∏ ; 1 - sum(Œ∏)]  # if Œ∏ does not sum to 1, we add the remainder here
	if any(p .< 0)
		warn("negative probability")
	end
	np = length(p)

	#¬†standard block
	for i = 0:n - np
		transition[i + 1,(i + 1):(i + np)] = p
	end

	for k in 1:(np-1)
		transition[n - k,(n - k):n] = [p[1:k]...; 1 - sum(p[1:k])]
	end
	transition[n,n] = 1.0
	return transition
end

